#include "pipex.h"

int	ft_strcmp(const char *a, const char *b, int n)
{
	int	i;

	i = 0;
	while (a[i] && b[i] && i < n)
	{
		if (a[i] != b[i])
			return (0);
		i++;
	}
	if (a[i] == '\n' && b[i] == 0)
		return (1);
	return (0);
}
